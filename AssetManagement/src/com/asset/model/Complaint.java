package com.asset.model;

public class Complaint {

	private String comId;
	private String empId;
	private String empName;
	private String comType;
	private String comDesc;
	private String comRecDt;
	private String comTarDt;
	private String comStat;
	
	public String getComId() {
		return comId;
	}
	public void setComId(String comId) {
		this.comId = comId;
	}
	public String getEmpId() {
		return empId;
	}
	public void setEmpId(String empId) {
		this.empId = empId;
	}
	public String getEmpName() {
		return empName;
	}
	public void setEmpName(String empName) {
		this.empName = empName;
	}
	public String getComType() {
		return comType;
	}
	public void setComType(String comType) {
		this.comType = comType;
	}
	public String getComDesc() {
		return comDesc;
	}
	public void setComDesc(String comDesc) {
		this.comDesc = comDesc;
	}
	public String getComRecDt() {
		return comRecDt;
	}
	public void setComRecDt(String comRecDt) {
		this.comRecDt = comRecDt;
	}
	public String getComTarDt() {
		return comTarDt;
	}
	public void setComTarDt(String comTarDt) {
		this.comTarDt = comTarDt;
	}
	public String getComStat() {
		return comStat;
	}
	public void setComStat(String comStat) {
		this.comStat = comStat;
	}
	
	
}
